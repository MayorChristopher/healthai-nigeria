'use client'
import { useState } from 'react'

type ShareButtonProps = {
  content: string
  isEmergency?: boolean
}

export default function ShareButton({ content, isEmergency }: ShareButtonProps) {
  const [copied, setCopied] = useState(false)

  const formatForSharing = (text: string) => {
    const header = 'ðŸ¥ HealthAI Nigeria - Medical Information\n' + '=' .repeat(45) + '\n\n'
    const footer = '\n\n' + '=' .repeat(45) + '\n' +
      'âš ï¸ DISCLAIMER: This is AI-generated health information.\n' +
      'Always consult a licensed healthcare professional for proper diagnosis and treatment.\n\n' +
      'ðŸš¨ For emergencies, call 112 immediately.\n\n' +
      'Source: HealthAI Nigeria (Powered by Google Gemini AI)\n' +
      'Visit: https://healthai-nigeria.vercel.app'
    
    return header + text + footer
  }

  const handleShare = async () => {
    const formattedText = formatForSharing(content)

    if (navigator.share) {
      try {
        await navigator.share({
          title: 'HealthAI Nigeria - Medical Information',
          text: formattedText
        })
      } catch (err) {
        // User cancelled or error - fallback to copy
        copyToClipboard(formattedText)
      }
    } else {
      copyToClipboard(formattedText)
    }
  }

  const copyToClipboard = (text: string) => {
    navigator.clipboard.writeText(text)
    setCopied(true)
    setTimeout(() => setCopied(false), 2000)
  }

  return (
    <button
      onClick={handleShare}
      className="text-xs text-gray-500 hover:text-gray-300 transition-colors cursor-pointer flex items-center gap-1"
      title="Share response"
    >
      {copied ? (
        <>
          <svg className="w-3 h-3" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 13l4 4L19 7" />
          </svg>
          <span>Copied</span>
        </>
      ) : (
        <>
          <svg className="w-3 h-3" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M8.684 13.342C8.886 12.938 9 12.482 9 12c0-.482-.114-.938-.316-1.342m0 2.684a3 3 0 110-2.684m0 2.684l6.632 3.316m-6.632-6l6.632-3.316m0 0a3 3 0 105.367-2.684 3 3 0 00-5.367 2.684zm0 9.316a3 3 0 105.368 2.684 3 3 0 00-5.368-2.684z" />
          </svg>
          <span>Share</span>
        </>
      )}
    </button>
  )
}
